public class TiposVariaveis {
    public static void main(String[] args) throws Exception {
        int numeroInteiro = 5;
        double numeroDecimal = 5.3;
        String palavra = "Huang";
        final double CONSTANTE = 3.14;

        System.out.println("O numero inteiro é: "+ numeroInteiro);
        System.out.println("O numero decimal é: "+ numeroDecimal);
        System.out.println("A palavra é: "+ palavra);
        System.out.println("O valor da constante é: "+ CONSTANTE);
    }
}
